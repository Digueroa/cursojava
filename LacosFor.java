package cursojava;
/**
 * Laços de Repetiçoes For
 * exemplo : Ao acrecentar j-- será feito um decremento.
 */

public class LacosFor {

	public static void main(String[] args) {
		System.out.println("Estruturas de Repetições");
		System.out.println("");
		System.out.println(" Exemplo: Uso do For");
		for (int j = 10; j> 0; j--) {
			System.out.println("hello"+j);
		}
		

	}

}
