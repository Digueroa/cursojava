package cursojava;
/**
 * Operadores Aritmeticos
 * = Setar uma variavel
 * + Soma
 * - subtração
 * * mutiplicaçao
 * / divisao
 *  % resto da divisao
 */
public class Operadoresaritimeticos {
	public static void main (String[] args) {
		int i =10;
		System.out.println("Resultado :" +i);
		i= 10+5;
		System.out.println("Resultado :" +i);
		i=10-5;
		System.out.println("Resultado :" +i);
		i=10*5;
		System.out.println("Resultado :" +i);
		i=10/5;
		System.out.println("Resultado :" +i);
		i=10%5;
		System.out.println("Resultado :" +i);
		
	}

}
